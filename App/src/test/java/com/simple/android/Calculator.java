public class Calculator {

    public int add(int one, int another) {
        return one + another ;
    }
    public int multiply(int one, int another) {
        return one * another;
    }

    public int subtract(int one, int another) {
        return one - another;
    }

    public int divide(int one, int another) {
        return one / another;
    }
}
